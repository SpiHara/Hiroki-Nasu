export const runtime = "edge"

const FAVICON_1X1_ICO = new Uint8Array([
  // ICONDIR
  0x00, 0x00, // reserved
  0x01, 0x00, // type = icon
  0x01, 0x00, // count
  // ICONDIRENTRY
  0x01, // width
  0x01, // height
  0x00, // color count
  0x00, // reserved
  0x01, 0x00, // planes
  0x20, 0x00, // bit count (32)
  0x30, 0x00, 0x00, 0x00, // bytes in res (48)
  0x16, 0x00, 0x00, 0x00, // image offset (22)
  // BITMAPINFOHEADER (40 bytes)
  0x28, 0x00, 0x00, 0x00, // header size
  0x01, 0x00, 0x00, 0x00, // width
  0x02, 0x00, 0x00, 0x00, // height (icon height * 2)
  0x01, 0x00, // planes
  0x20, 0x00, // bit count
  0x00, 0x00, 0x00, 0x00, // compression
  0x00, 0x00, 0x00, 0x00, // size image
  0x00, 0x00, 0x00, 0x00, // x pixels per meter
  0x00, 0x00, 0x00, 0x00, // y pixels per meter
  0x00, 0x00, 0x00, 0x00, // colors used
  0x00, 0x00, 0x00, 0x00, // important colors
  // Pixel (BGRA) - #7c3aed (purple) opaque
  0xed, 0x3a, 0x7c, 0xff,
  // AND mask (4 bytes, padded) - 0 = opaque
  0x00, 0x00, 0x00, 0x00,
])

export function GET() {
  return new Response(FAVICON_1X1_ICO, {
    status: 200,
    headers: {
      "content-type": "image/x-icon",
      "cache-control": "public, max-age=31536000, immutable",
    },
  })
}
